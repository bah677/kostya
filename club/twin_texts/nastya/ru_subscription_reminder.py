def churn18_ticket_body(*, label: str) -> str:
    return f"Сообщение «через 18 дней после выхода»: выбрана причина ухода — {label}."


def personalize_html(template: str, first_name: Optional[str]) -> str:
    safe = html.escape((first_name or "").strip() or "друг")
    return template.replace("{имя}", safe)


def churn18_button_label(key: str) -> str:
    lab = CHURN18_LABEL_BY_KEY.get(key, key)
    s = f"{key}. {lab}"
    if len(s) <= _CHURN18_BTN_MAX:
        return s
    return s[: _CHURN18_BTN_MAX - 1] + "…"
