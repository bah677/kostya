"""Единые правила цитирования Священного Писания в LLM-промптах."""

SCRIPTURE_FULL_VERSE_RULE = (
    "стих приводи ЦЕЛИКОМ: полный текст выбранного стиха (или каждого стиха подряд, "
    "если их несколько), без «…», без обрезки начала или конца, без одной выдернутой "
    "фразы из середины — это обязательно."
)

SCRIPTURE_BLOCKQUOTE_RULES = (
    "ЦИТАТЫ ИЗ СВЯЩЕННОГО ПИСАНИЯ (обязательно):\n"
    f"— {SCRIPTURE_FULL_VERSE_RULE}\n"
    "— каждая цитата только внутри <blockquote>...</blockquote>;\n"
    "— текст стиха — обычным текстом внутри blockquote;\n"
    "— ссылка на место (книга и стих) — с новой строки, курсивом: "
    "<i>(книга глава:стих)</i> внутри того же blockquote;\n"
    "— без blockquote цитату Писания не пиши; не заменяй кавычками или <i> без blockquote.\n"
    "Пример:\n"
    "<blockquote>Железо железо острит, и человек изощряет взгляд друга своего.\n\n"
    "<i>(Притч. 27:17)</i></blockquote>"
)

