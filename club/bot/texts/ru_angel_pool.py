def escape_name(name: str) -> str:
    return html.escape((name or "").strip() or "участник")


def currency_label(currency: str) -> str:
    cur = (currency or "").strip().upper()
    if cur == "RUB":
        return "₽"
    return "$"


def preset_amount_label(amount: int, currency: str) -> str:
    cur = (currency or "").strip().upper()
    if cur == "RUB":
        return f"{int(amount):,} ₽".replace(",", " ")
    return f"{int(amount)} $"


def format_amount(amount: float, currency: str) -> str:
    cur = (currency or "").strip().upper()
    if cur == "RUB":
        return f"{int(round(amount)):,}".replace(",", " ")
    if float(amount).is_integer():
        return str(int(amount))
    return f"{amount:.2f}".rstrip("0").rstrip(".")


def _plural_extensions(n: int) -> str:
    n = abs(int(n))
    mod10, mod100 = n % 10, n % 100
    if mod10 == 1 and mod100 != 11:
        return "продление"
    if 2 <= mod10 <= 4 and not (12 <= mod100 <= 14):
        return "продления"
    return "продлений"


def _plural_participants(n: int) -> str:
    n = abs(int(n))
    mod10, mod100 = n % 10, n % 100
    if mod10 == 1 and mod100 != 11:
        return "участнику"
    if 2 <= mod10 <= 4 and not (12 <= mod100 <= 14):
        return "участникам"
    return "участникам"


def slots_word(n: int) -> str:
    return _plural_extensions(n)


def delivered_word(n: int) -> str:
    return _plural_extensions(n)


def count_word(n: int) -> str:
    return _plural_participants(n)
